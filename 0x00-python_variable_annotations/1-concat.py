#!usr/bin/env python3
"""
concatenates two strings
"""


def concat(str1: str, str2: str) -> str:
    """concatenation of strings

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        _type_: add str1 and 2
    """
    adds: str = str1 + str2
    return adds
